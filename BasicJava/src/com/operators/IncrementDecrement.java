package com.operators;

public class IncrementDecrement {
	public static void main(String[] args) {
		
	
	int num = 10;
	//increment
	System.out.println(num++);
	System.out.println(num);
	System.out.println(++num);
	
	System.out.println();
	
	//decrement
	System.out.println(--num);
	System.out.println(num);
	System.out.println(num--);
	System.out.println(num);

	}
}
